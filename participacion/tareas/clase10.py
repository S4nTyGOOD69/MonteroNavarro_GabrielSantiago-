#metodos de utilidad
listas_frutas = ["manzana", "banana", "cereza", "durazno", "naranja"]
print(listas_frutas)

listas_frutas.append("kiwi") #agrega un elemento al final de la lista
print(listas_frutas)

listas_frutas.pop() #elimina el ultimo elemento de la lista
print(listas_frutas)

listas_frutas.pop(2) #elimina el elemento en la posicion 2(tercer elemento)
print(listas_frutas)



