#haz un programa que pida al usuario dos numero, y muestre su suma, 
# resta, multiplicacion y dicision 

num1 = float(input("ingrese primer numero: ") )
num2 = float(input("ingrese segundo numero: ") )


suma = num1 + num2 
print(f"La suma de {num1} + {num2} es : {suma}")


resta = num1 - num2 
print( f"La resta de {num1} - {num2} es {resta }")


multiplicacion = num1 * num2 
print(f"La multiplicacion de {num1} * {num2} es {multiplicacion}") 

divisionflotante = num1 / num2 
print(f"La divisionflotante de {num1} / {num2} es {divisionflotante}")
